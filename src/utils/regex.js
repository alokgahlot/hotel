export const reg={
    username:/^(?=[a-zA-Z0-9._ ]{4,20}$)(?!.*[_.]{2})[^_.].*[^_.]$/,
    email:/^\w+([\.-]?\w+)*@\w+([\.-]?\w+)*(\.\w{2,3})+$/,
    phone:/^[\+]?[(]?[0-9]{3}[)]?[-\s\.]?[0-9]{3}[-\s\.]?[0-9]{4,6}$/,
    password:/^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)(?=.*[@$!%*?&])[A-Za-z\d@$!%*?&]{8,}$/, 
    address:/[A-Za-z0-9'.\-\s,]/
}